package Java_Programming_Assessment_MCQ;

/*
Arithmetic and Assignment Operators
Create a Java program that demonstrates various arithmetic and assignment operators.

**Requirements:**
- Use +, -, *, /, % operators
- Demonstrate assignment operators (=, +=, -=, *=, /=)
- Show the results of each operation
 */
public class Q2_Arithmetic_and_Assignment_Operators {

    public static void main(String[] args) {

        int a = 10;
        int b = 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
    }
}


